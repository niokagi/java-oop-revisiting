package fifth_week.source_code.module_10.tasks;

public class Kendaraan {
    protected int jmlRoda;
    protected String warna;

    public Kendaraan(int jmlRoda, String warna) {
        this.jmlRoda = jmlRoda;
        this.warna = warna;
    }
    public int getJmlRoda() { return this.jmlRoda; }
    public String getWarna() { return this.warna; }
}






